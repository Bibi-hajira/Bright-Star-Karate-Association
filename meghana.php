    <h1>Coach of BSKA</h1>
    

    <p>   
    </br></br></br> Seito Meghana M B began taking interest in Martial Arts his senior year of high school. He began training Tae Kwon Do in June of 1971 under the instruction of the late Myong Kil Kim. Mr. Meghana M B trained very hard, generally practicing 5 hours a day everyday to earn his first degree Black Belt in 1973, his second in 1975, and his third in 1978. During this time Seito Meghana M B also studied Mu Do Kwon Tang Soo Do to learn more about martial arts. Sensei Meghana M B began teaching Martial Arts to others in 1977 and opened his own school in 1978.</br></br>

In 1979, Seito Meghana M B began learning Kobayashi Shorin Ryu under the instruction of Bill Mattix. Seito worked very hard for almost 2 years and earned his First Degree Black Belt under Bill Mattix. In 1982, Seito Meghana M B changed Meghana M B’s Karate to the style of Kobayashi Shorin Ryu. Seito Meghana M B also studied Kobayashi Shorin Ryu under Tadashi Yamashita. Seito Meghana M B spent another 16 years in the style, eventually earning all of his ranks up to 6th Degree Black Belt.</br></br>

In 1997, Seito Meghana M B began training under John Hutchcroft, in the style of Matsumura Seito Shorin Ryu. In 1999, Seito Meghana M B changed Meghana M B’s Karate to the style of Matsumura Seito Shorin Ryu. In 2001, Seito Meghana M B achieved the rank of 6th Degree Black Belt in the style of Matsumura Seito Shorin Ryu.</br></br>

